package maruf.com.myapplication.rayonbuntu;

public class setkas {


    String idkas;
    String nama;
    String ranting;
    String jumlah;
    String desk;

    String title;
    public setkas() {
    }


    public String getIdkas() {
        return idkas;
    }

    public void setIdkas(String idkas) {
        this.idkas = idkas;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getRanting() {
        return ranting;
    }

    public void setRanting(String ranting) {
        this.ranting = ranting;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getDesk() {
        return desk;
    }

    public void setDesk(String desk) {
        this.desk = desk;
    }
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public setkas(String idkas, String nama, String ranting, String jumlah, String desk, String title) {
        this.idkas = idkas;
        this.nama = nama;
        this.ranting = ranting;
        this.jumlah = jumlah;
        this.desk = desk;
        this.title = title;
    }
}
